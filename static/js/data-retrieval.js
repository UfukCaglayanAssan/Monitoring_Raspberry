    async handleGetData() {
        if (!this.validateForm()) {
            this.showToast('L√ºtfen t√ºm alanlarƒ± doldurunuz', 'error');
            return;
        }

        const arm = document.getElementById('dataArmSelect').value;
        const address = document.getElementById('dataAddressInput').value;
        const value = document.getElementById('dataValueSelect').value;

        this.showLoading('Veri alma komutu g√∂nderiliyor...');

        try {
            const response = await fetch('/api/datagets', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                },
                body: JSON.stringify({
                    armValue: parseInt(arm),
                    slaveAddress: parseInt(address),
                    slaveCommand: parseInt(value)
                })
            });

            if (response.ok) {
                const valueText = this.getDataTypeText(value);
                // Toast bildirimi kaldƒ±rƒ±ldƒ±
                
                // Komut g√∂nderildiƒüi zamanƒ± kaydet
                const commandTimestamp = Date.now();
                console.log(`üïê Komut g√∂nderildi: ${new Date(commandTimestamp).toLocaleString()}`);
                
                // Tekil veri alma - sadece 3 saniye bekle
                this.showLoading('Veri bekleniyor...');
                await this.waitForSingleData(parseInt(arm), parseInt(address), parseInt(value), valueText, commandTimestamp);
                
                // Formu temizle
                this.clearForm();
            } else {
                throw new Error('Komut g√∂nderilemedi');
            }
        } catch (error) {
            console.error('GetData hatasƒ±:', error);
            this.showToast('Komut g√∂nderilirken hata olu≈ütu', 'error');
        } finally {
            this.hideLoading();
        }
    }

    async waitForSingleData(arm, address, value, valueText, commandTimestamp) {
        const maxAttempts = 2; // 2 deneme (3 saniye + 3 saniye)
        let attempt = 0;
        
        while (attempt < maxAttempts) {
            attempt++;
            console.log(`üîç Tekil veri bekleme - Deneme ${attempt}/${maxAttempts}`);
            
            // 3 saniye bekle
            await new Promise(resolve => setTimeout(resolve, 3000));
            
            // Veri gelip gelmediƒüini kontrol et
            const data = await this.checkForSingleData(arm, address, value, commandTimestamp);
            
            if (data) {
                console.log('‚úÖ Tekil veri alƒ±ndƒ±:', data);
                this.showToast(`${valueText} verisi alƒ±ndƒ±: ${data}`, 'success');
                return data;
            }
            
            if (attempt < maxAttempts) {
                console.log('‚è≥ Veri gelmedi, tekrar denenecek...');
                // Deneme sayƒ±sƒ± g√∂sterilmiyor, sadece ba≈ülangƒ±√ß mesajƒ±
                this.showLoading('Veri bekleniyor...');
            }
        }
        
        console.log('‚ùå Tekil veri alƒ±namadƒ±');
        this.showToast('Veri alƒ±namadƒ±, l√ºtfen tekrar deneyin', 'error');
        return null;
    }

    async checkForSingleData(arm, address, value, commandTimestamp) {
        try {
            // Tekil veri alma i√ßin doƒürudan batarya verilerini kontrol et
            const response = await fetch('/api/batteries', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({})
            });
            
            if (response.ok) {
                const result = await response.json();
                if (result.success && result.data) {
                    // G√∂nderilen adresin 1 fazlasƒ±na bak (k-2 mantƒ±ƒüƒ± nedeniyle)
                    const targetAddress = parseInt(address) + 1;
                    
                    console.log(`üîç Tekil veri arama: Kol ${arm}, G√∂nderilen adres ${address}, Aranan adres ${targetAddress}, Tip ${value}`);
                    console.log(`üïê Komut zamanƒ±: ${new Date(commandTimestamp).toLocaleString()}`);
                    console.log(`üîç Mevcut bataryalar:`, result.data.map(b => ({arm: b.arm, address: b.address})));
                    
                    // ƒ∞lgili kol ve adrese sahip bataryayƒ± ara
                    const targetBattery = result.data.find(battery => 
                        battery.arm == arm && 
                        battery.address == targetAddress
                    );
                    
                    if (targetBattery && targetBattery.entries) {
                        // Komut g√∂nderildikten sonraki verileri kontrol et
                        const recentEntry = targetBattery.entries.find(entry => {
                            const entryTime = new Date(entry.timestamp).getTime();
                            return entryTime >= commandTimestamp; // Komut g√∂nderildikten sonraki veriler
                        });
                        
                        if (recentEntry) {
                            // Deƒüer tipine g√∂re veriyi al
                            const dataValue = this.getDataValueFromEntry(recentEntry, value);
                            if (dataValue !== null) {
                                console.log(`‚úÖ Tekil veri bulundu: Kol ${arm}, Adres ${targetAddress}, Tip ${value}, Deƒüer ${dataValue}`);
                                console.log(`üïê Veri zamanƒ±: ${new Date(recentEntry.timestamp).toLocaleString()}`);
                                return dataValue;
                            }
                        }
                        
                        console.log(`‚ùå Tekil veri bulunamadƒ±: Kol ${arm}, Adres ${targetAddress}, Tip ${value} - Komut g√∂nderildikten sonra veri yok`);
                    } else {
                        console.log(`‚ùå Batarya bulunamadƒ±: Kol ${arm}, Adres ${targetAddress}`);
                    }
                }
            }
        } catch (error) {
            console.error('Tekil veri kontrol hatasƒ±:', error);
        }
        
        return null;
    }

    getDataValueFromEntry(entry, value) {
        // entry.entries array'inden ilgili dtype'ƒ± bul
        const targetEntry = entry.entries.find(e => e.dtype == value);
        return targetEntry ? targetEntry.data : null;
    }

    getDataValueByType(data, value) {
        const valueMap = {
            '10': data.voltage,
            '11': data.health_status,
            '12': data.temperature,
            '13': data.positive_pole_temp,
            '14': data.negative_pole_temp,
            '15': data.ntc3_temp,
            '126': data.charge_status
        };
        
        return valueMap[value] || null;
    }

