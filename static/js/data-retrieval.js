    async stopDataRetrieval() {
        try {
            // Backend'e veri alma modunu durdur
            const response = await fetch('/api/stop-data-retrieval', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                }
            });

            if (response.ok) {
                this.isDataRetrievalMode = false;
                this.retrievalConfig = null;
                this.retrievedData = [];
                
                // Ana sayfaya d√∂n
                this.hideOperationsList();
                
                console.log('üõë Veri alma modu durduruldu');
            } else {
                throw new Error('Veri alma modu durdurulamadƒ±');
            }
        } catch (error) {
            console.error('Veri alma modu durdurma hatasƒ±:', error);
            this.showToast('Veri alma modu durdurulamadƒ±', 'error');
        }
    }

