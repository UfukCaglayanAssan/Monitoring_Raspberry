// Batarya Loglarƒ± Sayfasƒ± JavaScript
// Class'ƒ±n zaten tanƒ±mlanƒ±p tanƒ±mlanmadƒ±ƒüƒ±nƒ± kontrol et
if (typeof window.BatteryLogsPage === 'undefined') {
    window.BatteryLogsPage = class BatteryLogsPage {
    constructor() {
        this.currentPage = 1;
        this.pageSize = 50;
        this.totalPages = 1;
        this.filters = {
            arm: '',
            battery: '',
            startDate: '',
            endDate: ''
        };
        this.logs = [];
        
        this.init();
    }

    init() {
        console.log('üöÄ BatteryLogsPage init() √ßaƒürƒ±ldƒ±');
        this.bindEvents();
        this.setDefaultDates();
        this.initSelect2();
        this.loadArmOptions();
        this.loadLogs();
    }

    bindEvents() {
        // Filtre butonlarƒ±
        document.getElementById('applyFilters').addEventListener('click', () => {
            this.applyFilters();
        });

        document.getElementById('clearFilters').addEventListener('click', () => {
            this.clearFilters();
        });

        // Tablo butonlarƒ±
        document.getElementById('refreshLogs').addEventListener('click', () => {
            this.loadLogs();
        });

        document.getElementById('exportLogs').addEventListener('click', () => {
            console.log('Export butonu tƒ±klandƒ±!');
            this.exportLogs();
        });

        // Sayfalama
        document.getElementById('prevPage').addEventListener('click', () => {
            this.previousPage();
        });

        document.getElementById('nextPage').addEventListener('click', () => {
            this.nextPage();
        });

        // Filtre input'larƒ±
        document.getElementById('armFilter').addEventListener('change', (e) => {
            this.filters.arm = e.target.value;
            this.updateBatteryOptions(e.target.value);
        });

        // Select2 i√ßin jQuery event kullan
        $('#batteryFilter').on('change', (e) => {
            this.filters.battery = $(e.target).val();
        });

        document.getElementById('startDate').addEventListener('change', (e) => {
            this.filters.startDate = e.target.value;
        });

        document.getElementById('endDate').addEventListener('change', (e) => {
            this.filters.endDate = e.target.value;
        });

        // Dil deƒüi≈üikliƒüi dinleyicisi
        window.addEventListener('languageChanged', (e) => {
            this.onLanguageChanged(e.detail.language);
        });
    }

    setDefaultDates() {
        // Varsayƒ±lan tarih filtresi uygulanmaz - t√ºm veriler g√∂sterilir
        document.getElementById('startDate').value = '';
        document.getElementById('endDate').value = '';
        
        this.filters.startDate = '';
        this.filters.endDate = '';
    }

    formatDateForInput(date) {
        return date.toISOString().split('T')[0];
    }

    onLanguageChanged(language) {
        console.log('BatteryLogs: Dil deƒüi≈üti:', language);
        this.updateUITexts(language);
    }

    updateUITexts(language) {
        // UI metinlerini g√ºncelle
        const elements = document.querySelectorAll('[data-tr], [data-en]');
        elements.forEach(element => {
            if (language === 'en' && element.hasAttribute('data-en')) {
                element.textContent = element.getAttribute('data-en');
            } else if (language === 'tr' && element.hasAttribute('data-tr')) {
                element.textContent = element.getAttribute('data-tr');
            }
        });
    }

    async loadLogs() {
        console.log('üîç loadLogs() √ßaƒürƒ±ldƒ±');
        const tableBody = document.getElementById('batteryLogsTableBody');
        
        try {
            console.log('üìä API √ßaƒürƒ±sƒ± yapƒ±lƒ±yor...');
            this.showLoading(tableBody);

            const currentLanguage = localStorage.getItem('language') || 'tr';
            
            const response = await fetch('/api/battery-logs', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'X-Language': currentLanguage
                },
                body: JSON.stringify({
                    page: this.currentPage,
                    pageSize: this.pageSize,
                    filters: this.filters
                })
            });

            if (response.ok) {
                const data = await response.json();
                console.log('Battery logs API response:', data);
                this.logs = data.logs || [];
                this.totalPages = data.totalPages || 1;
                
                console.log('Logs loaded:', this.logs.length, 'items');
                this.renderLogs();
                this.updatePagination();
            } else {
                throw new Error('Log verileri alƒ±namadƒ±');
            }
        } catch (error) {
            console.error('Log y√ºkleme hatasƒ±:', error);
            this.showError(tableBody, 'Log verileri y√ºklenirken bir hata olu≈ütu.');
        }
    }

    renderLogs() {
        const tableBody = document.getElementById('batteryLogsTableBody');
        
        if (this.logs.length === 0) {
            const currentLanguage = localStorage.getItem('language') || 'tr';
            tableBody.innerHTML = `
                <tr>
                    <td colspan="9">
                        <div class="empty-state">
                            <i class="fas fa-inbox"></i>
                            <h4>${currentLanguage === 'en' ? 'No Data Found' : 'Veri Bulunamadƒ±'}</h4>
                            <p>${currentLanguage === 'en' ? 'No log data found matching the selected criteria.' : 'Se√ßilen kriterlere uygun log verisi bulunamadƒ±.'}</p>
                        </div>
                    </td>
                </tr>
            `;
            return;
        }

        // DOM element kontrol√º
        if (!tableBody) {
            console.error('‚ùå tableBody element bulunamadƒ±!');
            return;
        }
        
        tableBody.innerHTML = this.logs.map(log => {
            return `
                <tr>
                    <td>${this.formatDate(log.timestamp)}</td>
                    <td>${log.arm}</td>
                    <td>${log.batteryAddress}</td>
                    <td>${this.formatValue(log.voltage, 'V')}</td>
                    <td>${this.formatValue(log.charge_status, '%')}</td>
                    <td>${this.formatValue(log.temperature, '¬∞C')}</td>
                    <td>${this.formatValue(log.positive_pole_temp, '¬∞C')}</td>
                    <td>${this.formatValue(log.negative_pole_temp, '¬∞C')}</td>
                    <td>${this.formatValue(log.health_status, '%')}</td>
                </tr>
            `;
        }).join('');
    }

    formatValue(value, unit) {
        if (value === null || value === undefined) {
            return '-';
        }
        return `${parseFloat(value).toFixed(3)} ${unit}`;
    }

    formatDate(timestamp) {
        const date = new Date(timestamp);
        return date.toLocaleDateString('tr-TR', {
            day: '2-digit',
            month: '2-digit',
            year: 'numeric',
            hour: '2-digit',
            minute: '2-digit',
            second: '2-digit'
        });
    }

    showLoading(tableBody) {
        tableBody.innerHTML = `
            <tr>
                <td colspan="9" class="text-center">
                    <div class="loading-spinner">
                        <i class="fas fa-spinner fa-spin"></i>
                        <span>Y√ºkleniyor...</span>
                    </div>
                </td>
            </tr>
        `;
    }

    showError(tableBody, message) {
        tableBody.innerHTML = `
            <tr>
                <td colspan="9" class="text-center text-danger">
                    <i class="fas fa-exclamation-triangle"></i>
                    <span>${message}</span>
                </td>
            </tr>
        `;
    }

    updatePagination() {
        document.getElementById('currentPage').textContent = this.currentPage;
        document.getElementById('totalPages').textContent = this.totalPages;
        
        document.getElementById('prevPage').disabled = this.currentPage <= 1;
        document.getElementById('nextPage').disabled = this.currentPage >= this.totalPages;
    }

    previousPage() {
        if (this.currentPage > 1) {
            this.currentPage--;
            this.loadLogs();
        }
    }

    nextPage() {
        if (this.currentPage < this.totalPages) {
            this.currentPage++;
            this.loadLogs();
        }
    }

    applyFilters() {
        // Filtre deƒüerlerini g√ºncelle
        this.filters.arm = document.getElementById('armFilter').value;
        this.filters.battery = $('#batteryFilter').val();
        this.filters.startDate = document.getElementById('startDate').value;
        this.filters.endDate = document.getElementById('endDate').value;
        
        console.log('üîç Filtreler uygulandƒ±:', this.filters);
        
        this.currentPage = 1;
        this.loadLogs();
    }

    clearFilters() {
        console.log('üßπ clearFilters() √ßaƒürƒ±ldƒ±');
        this.filters = {
            arm: '',
            battery: '',
            startDate: '',
            endDate: ''
        };
        
        document.getElementById('armFilter').value = '';
        $('#batteryFilter').val('').trigger('change');
        this.setDefaultDates();
        
        // Batarya se√ßeneklerini sƒ±fƒ±rla
        this.updateBatteryOptions('');
        
        this.currentPage = 1;
        this.loadLogs();
        console.log('‚úÖ Filtreler temizlendi');
    }

    async loadArmOptions() {
        try {
            const response = await fetch('/api/active-arms', {
                method: 'GET',
                headers: { 'Content-Type': 'application/json' }
            });
            
            if (response.ok) {
                const data = await response.json();
                if (data.success && data.activeArms) {
                    const armFilter = document.getElementById('armFilter');
                    const currentArmValue = this.filters.arm || armFilter.value;
                    
                    // Event listener'ƒ± kaldƒ±r (yeniden baƒülanacak)
                    const oldArmFilter = armFilter.cloneNode(false);
                    
                    // Kol se√ßeneklerini g√ºncelle
                    armFilter.innerHTML = '<option value="">T√ºm Kollar</option>';
                    data.activeArms.forEach(arm => {
                        if (arm.slave_count > 0) {
                            const option = document.createElement('option');
                            option.value = arm.arm;
                            option.textContent = `Kol ${arm.arm}`;
                            armFilter.appendChild(option);
                        }
                    });
                    
                    // Event listener'ƒ± yeniden baƒüla
                    armFilter.addEventListener('change', (e) => {
                        this.filters.arm = e.target.value;
                        this.updateBatteryOptions(e.target.value);
                    });
                    
                    // Eƒüer daha √∂nce bir kol se√ßilmi≈üse, onu geri y√ºkle ve batarya se√ßeneklerini g√ºncelle
                    if (currentArmValue) {
                        armFilter.value = currentArmValue;
                        this.filters.arm = currentArmValue;
                        
                        // Batarya se√ßimini de sakla
                        const currentBatteryValue = this.filters.battery || $('#batteryFilter').val();
                        
                        await this.updateBatteryOptions(currentArmValue);
                        
                        // Batarya se√ßimini geri y√ºkle
                        if (currentBatteryValue) {
                            $('#batteryFilter').val(currentBatteryValue).trigger('change');
                            this.filters.battery = currentBatteryValue;
                        }
                    } else {
                        // Kol se√ßilmemi≈üse batarya se√ßeneƒüini devre dƒ±≈üƒ± bƒ±rak
                        await this.updateBatteryOptions('');
                    }
                }
            }
        } catch (error) {
            console.error('‚ùå Kol se√ßenekleri y√ºkleme hatasƒ±:', error);
        }
    }
    
    initSelect2() {
        // Batarya select2'yi ba≈ülat
        $('#batteryFilter').select2({
            placeholder: '√ñnce kol se√ßiniz',
            allowClear: true,
            width: '100%'
        });
    }

    async updateBatteryOptions(selectedArm) {
        const batteryFilter = document.getElementById('batteryFilter');
        
        if (!selectedArm) {
            $('#batteryFilter').empty();
            $('#batteryFilter').append('<option value="">√ñnce kol se√ßiniz</option>');
            $('#batteryFilter').select2({
                placeholder: '√ñnce kol se√ßiniz',
                allowClear: true,
                width: '100%'
            });
            batteryFilter.disabled = true;
            return;
        }
        
        try {
            const response = await fetch('/api/active-arms');
            const data = await response.json();
            
            if (data.success && data.activeArms) {
                const selectedArmData = data.activeArms.find(arm => arm.arm == selectedArm);
                const batteryCount = selectedArmData ? selectedArmData.slave_count : 0;
                
                if (batteryCount > 0) {
                    // Select2'yi temizle
                    $('#batteryFilter').empty();
                    
                    // T√ºm Bataryalar se√ßeneƒüi ekle
                    $('#batteryFilter').append('<option value="">T√ºm Bataryalar</option>');
                    
                    // Batarya se√ßeneklerini ekle (1'den ba≈üla)
                    for (let i = 1; i <= batteryCount; i++) {
                        $('#batteryFilter').append(`<option value="${i}">Batarya ${i}</option>`);
                    }
                    
                    // Select2'yi yeniden ba≈ülat
                    $('#batteryFilter').select2({
                        placeholder: 'Batarya se√ßiniz',
                        allowClear: true,
                        width: '100%'
                    });
                    
                    batteryFilter.disabled = false;
                } else {
                    $('#batteryFilter').empty();
                    $('#batteryFilter').append('<option value="">Bu kolda batarya yok</option>');
                    $('#batteryFilter').select2({
                        placeholder: 'Bu kolda batarya yok',
                        allowClear: true,
                        width: '100%'
                    });
                    batteryFilter.disabled = true;
                }
            }
        } catch (error) {
            console.error('Batarya se√ßenekleri y√ºkleme hatasƒ±:', error);
            $('#batteryFilter').empty();
            $('#batteryFilter').append('<option value="">Hata olu≈ütu</option>');
            $('#batteryFilter').select2({
                placeholder: 'Hata olu≈ütu',
                allowClear: true,
                width: '100%'
            });
            batteryFilter.disabled = true;
        }
    }

    exportLogs() {
        // CSV export i≈ülemi
        console.log('Export i≈ülemi ba≈ülatƒ±ldƒ±');
        console.log('Filtreler:', this.filters);
        
        try {
            // Filtreleri hazƒ±rla
            const exportFilters = {
                arm: this.filters.arm || '',
                battery: this.filters.battery || '',
                start_date: this.filters.startDate || '',
                end_date: this.filters.endDate || ''
            };
            
            // API'ye export isteƒüi g√∂nder
            fetch('/api/battery-logs/export', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                },
                body: JSON.stringify({
                    filters: exportFilters
                })
            })
            .then(response => {
                if (response.ok) {
                    return response.blob();
                }
                throw new Error('Export hatasƒ±: ' + response.status);
            })
            .then(blob => {
                // CSV dosyasƒ±nƒ± indir
                const url = window.URL.createObjectURL(blob);
                const a = document.createElement('a');
                a.href = url;
                a.download = `battery_logs_export_${new Date().toISOString().split('T')[0]}.csv`;
                document.body.appendChild(a);
                a.click();
                window.URL.revokeObjectURL(url);
                document.body.removeChild(a);
                
                console.log('Export ba≈üarƒ±lƒ±');
            })
            .catch(error => {
                console.error('Export hatasƒ±:', error);
                alert('Export sƒ±rasƒ±nda hata olu≈ütu: ' + error.message);
            });
            
        } catch (error) {
            console.error('Export hatasƒ±:', error);
            alert('Export sƒ±rasƒ±nda hata olu≈ütu: ' + error.message);
        }
    }
    }; // Class kapanƒ±≈ü s√ºsl√º parantezi
} // if statement kapanƒ±≈ü s√ºsl√º parantezi

// Sayfa y√ºklendiƒüinde ba≈ülat
function initBatteryLogsPage() {
    console.log('üîß initBatteryLogsPage() √ßaƒürƒ±ldƒ±');
    if (!window.batteryLogsPage) {
        console.log('üÜï Yeni BatteryLogsPage instance olu≈üturuluyor');
        window.batteryLogsPage = new BatteryLogsPage();
    } else {
        // Mevcut instance varsa kol se√ßeneklerini yenile ve veri y√ºkle
        console.log('üîÑ Mevcut BatteryLogsPage instance kullanƒ±lƒ±yor, kol se√ßenekleri yenileniyor');
        // Kol se√ßeneklerini yenile
        window.batteryLogsPage.loadArmOptions();
        // Veri y√ºkle
        window.batteryLogsPage.loadLogs();
    }
}

// Global olarak eri≈üilebilir yap
window.initBatteryLogsPage = initBatteryLogsPage;

// Script y√ºklendiƒüinde otomatik init
console.log('üîß Battery-logs.js y√ºklendi, otomatik init ba≈ülatƒ±lƒ±yor...');
initBatteryLogsPage();